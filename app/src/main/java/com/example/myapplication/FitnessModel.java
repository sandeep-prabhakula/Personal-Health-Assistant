package com.example.myapplication;

public class FitnessModel {
    private int img;
    private String name;

    public FitnessModel(int img, String name) {
        this.img = img;
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public String getName() {
        return name;
    }
}
